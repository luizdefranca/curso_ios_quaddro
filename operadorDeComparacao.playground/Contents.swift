/*
 
 Operador de comparação

 O resultado de uma expressao lógica pode ser representado de duas formas
 true - > resultado é verdadeiro
 false - > resultado é falso
 
 */

var variavel1 = 3
var variavel2 = 2


// igual a (==)
print(variavel1 == variavel2)

//Menor que ( < )
print(variavel1 < variavel2)

//Maior que
print(variavel1 > variavel2)

//Menor ou igual
print(variavel1 <= variavel2)

//Maior ou igual
print(variavel1 >= variavel2)

//Diferente
print(variavel1 != variavel2)

//Descobrindo se um numero é par

print("A variável 1 é par? \(variavel1 % 2 == 0)")
print("A variável 2 é par? \(variavel2 % 2 == 0)")



