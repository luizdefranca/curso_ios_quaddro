import Foundation

/*
 
 Repeat while
 
 sintaxe:
 
 var variavelDeControle
 repeat{
    bloco repetido
 }while(condicao de parada)
 
 */

var numero = 100

repeat{
    print(numero)
    numero += 1
}while (numero < 10)


